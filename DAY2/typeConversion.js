// Type conversion is the process of explicitly making conversion from one data type to another
// Example
console.log(Number('42')); // string to number
console.log(String(100)); // number to string
console.log(Boolean(0.1)); // number to boolean. 0 returns False while non-zero result to True
console.log(parseInt('23.45')); // string to integer
console.log(parseFloat('23.45')); // string to decimal
